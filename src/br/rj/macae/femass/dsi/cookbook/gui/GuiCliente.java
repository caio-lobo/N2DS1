    private void btmEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmEditarActionPerformed
        // TODO add your handling code here:
        if(tblClientes.getSelectedRowCount()>0){
            limparCampos();
                liberarCampos();
            ClienteControle controle = new ClienteControle();
            Long v = (Long) ((DefaultTableModel) tblClientes.getModel()).getValueAt(tblClientes.getSelectedRow(), 1);
            
            try {
                
                ClienteE c;
                c = controle.getCategoriaPorId(v);
                
                txtDependentes.setText(c.getDependentes().toString());
                txtEndereco.setText(c.getEndereco());
                txtID.setText(c.getId().toString());
                txtNome.setText(c.getNome());
                txtTelefone.setText(c.getTelefone());
                
                liberarCampos();
            } catch (SQLException ex) {
                Logger.getLogger(GuiCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            btmEditSet();
        }
    }//GEN-LAST:event_btmEditarActionPerformed


