    private void btmSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmSalvarActionPerformed
        // TODO add your handling code here:
        ClienteControle controle = new ClienteControle();
        try {
            ClienteE c = new ClienteE();
            
            c.setDependentes(Integer.parseInt(txtDependentes.getText()));
            c.setNome(txtNome.getText());
            c.setEndereco(txtEndereco.getText());
            c.setTelefone(txtTelefone.getText());
            
            c.setId(txtID.getText().equals("")? null:Long.parseLong(txtID.getText()));
        
            controle.gravar(c);
        } catch (SQLException ex) {
            Logger.getLogger(GuiCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
           
        try {
            carregarLista();
        } catch (SQLException ex) {
            Logger.getLogger(GuiCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        buttonStartSet();
        bloquearCampos();
        
    }//GEN-LAST:event_btmSalvarActionPerformed

