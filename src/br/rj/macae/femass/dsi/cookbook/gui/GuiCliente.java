    private void bloquearCampos() {
    txtDependentes.setEnabled(false); 
    txtEndereco.setEnabled(false);
    txtID.setEnabled(false);
    txtNome.setEnabled(false);
    txtTelefone.setEnabled(false);
    
    }

    private void limparCampos() {
    
        txtDependentes.setText("");
        txtEndereco.setText("");
        txtID.setText("");
        txtNome.setText("");
        txtTelefone.setText("");
     
    }

    private void carregarLista() throws SQLException {
     ClienteControle controle = new ClienteControle();
    
     try{
         controle.atualizarLista(tblClientes);
         
     }catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }         
}

    private void buttonStartSet() {
        
        btmCancelar.setEnabled(false);
        btmEditar.setEnabled(true);
        btmNovo.setEnabled(true);
        btmSalvar.setEnabled(false);
        
    }

    private void liberarCampos() {
     txtDependentes.setEnabled(true); 
    txtEndereco.setEnabled(true);
    txtID.setEnabled(true);
    txtNome.setEnabled(true);
    txtTelefone.setEnabled(true);}

    private void btmEditSet() {
     
        btmCancelar.setEnabled(true);
        btmEditar.setEnabled(false);
        btmNovo.setEnabled(false);
        btmSalvar.setEnabled(true);
    }
